list6 = [5, -41, 61, 35, -612, 651, -213, -21]

first = -1
last = -1

# Переборка списка, нахожу первое и последнее положительное
# число

for i in range(len(list6)):
    if list6[i] > 0:
        if first == -1:
            first = i
        last = i



if first != -1 and last != -1 and first < last:
    result3 = 0
    for i in list6[first + 1 : last]:
        result3 += i
    print("Сумма между: ", result3)
else:
    print("Менее двух положительных!")