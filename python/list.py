# Как я понял len считает элементы в списке, цикл перебирает
# Элементы. Если элемент кратен 3 то list4[i] берет элемент
# по индексу, *= умножает все найденые числа

list4 = [452, 23, 31, 41, 61, 12, 51, 541, 41, 12]
result = 1
for i in range(len(list4)):
    if i % 3 == 0:
        result *= list4[i]

print(result)

