# Создал список с рандомными числами + пустой список
# чтобы потом вывести сумму отрицательных чисел.
# Создал цикл для элементоов в списке list1,
# если в этом списке элемент меньше 0, то в новый
# список "summa" добавляется отрицательные числа из
# списка list1. Сложил с помощю метода sum список "summa"

list1 = [-1, 2, -65, 12, 145, -51, 123, -42]
summa = []

for i in list1:
    if i < 0:
        summa.append(i)

summa = sum(summa)
print(summa)

